#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	s = (char *)s;
	if ((char)c == '\0')
		return ((char *)(s + ft_strlen(s)));
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
